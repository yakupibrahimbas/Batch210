package MyPrivateWork;

public class Y04 {
    public static void main(String[] args) {

        int i = 1;
        while (i < 10) {
            System.out.println(i);
            i += 2;
        }
        System.out.println("While döngüsü bitti");
        int j = 20;
        do {

            System.out.println(j);
            j += 2;
        } while (j < 100);
        System.out.println("Do while döngüsü bitti");
    }
}



