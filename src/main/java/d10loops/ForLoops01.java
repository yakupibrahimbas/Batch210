package d10loops;

public class ForLoops01 {
    public static void main(String[] args) {



        for (int i = 3; i < 21; i++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }
        System.out.println("---------");
        for (int i=4; i<21; i++){
            System.out.println(i);
        }

    }
}
